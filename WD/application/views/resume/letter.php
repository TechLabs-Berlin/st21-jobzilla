 <section class="example-section example-section-heading">
 	<h2>Graphic Designer Cover Letter Tips</h2>
 	<p>Before you start writing, make sure you know <a href="https://resumegenius.com/blog/cover-letter-help/how-to-write-a-cover-letter">how to write a cover letter</a> in a way that makes you seem like the best candidate for the job.</p>
 </section>
 <article id="tips" class="example-section example-section-tips">
 	<div class="entry-content example-section-tips-inner">
 		<p>You’re a fantastic visual communicator and your portfolio shows it. But before many hiring managers even take a look at your portfolio, they’ll assess your graphic designer skills and qualifications by reading your cover letter.</p>
 		<p>Follow these three tips to write a graphic designer cover letter to increase your chances of landing a fulfilling position where you can apply your creativity to a brand, product, or website:</p>
 		<h3>1. Highlight your top graphic designer skills throughout your cover letter</h3>
 		<p>As a graphic designer, your duties can vary depending on the type of visual communications you specialize in — from designing web pages to creating graphics for logos or advertisements.</p>
 		<p>To show employers you’re the right designer for the job, it’s critical to mention job-specific skills throughout your cover letter.</p>
 		<p>For example, when hiring for a UI graphic designer, employers seek designers with the right skill-set to create user-friendly designs. In this case, showcase your skills and experience in User Interface design and include key <a href="https://resumegenius.com/blog/resume-help/hard-skills">hard skills</a> like CSS, HTML, and JavaScript in your cover letter.</p>
 		<p>In your cover letter body, highlight a variety of graphic designer <a href="https://resumegenius.com/blog/resume-help/soft-skills">soft skills</a> when detailing your previous work experience. For example, as a logo designer, your creativity goes hand-in-hand with the ability to receive criticism for conceptualizing designs and satisfying client requirements.</p>
 		<p>Here’s an example of how to include these skills in your cover letter:</p>
 		<blockquote><p>I possess a wide range of abilities that combine innovative art and design principles. Furthermore, my expert knowledge of Adobe Creative Suite, HTML, and CSS will allow me to play a crucial role in the implementation of your design initiatives.</p></blockquote>
 		<p>Here are important graphic designer soft skills:</p>
 		<ul>
 			<li aria-level="1"><a href="https://resumegenius.com/blog/resume-help/communication-skills">Communication skills</a></li>
 			<li aria-level="1">Attention to detail</li>
 			<li aria-level="1"><a href="https://resumegenius.com/blog/resume-help/interpersonal-skills">Interpersonal skills</a></li>
 			<li aria-level="1"><a href="https://resumegenius.com/blog/resume-help/customer-service-skills">Customer service skills</a></li>
 			<li aria-level="1">Decision-making</li>
 			<li aria-level="1">Problem-solving skills</li>
 			<li aria-level="1">Multitasking</li>
 			<li aria-level="1">Collaboration</li>
 			<li aria-level="1"><a href="https://resumegenius.com/blog/resume-help/time-management-skills">Time management skills</a></li>
 			<li aria-level="1"><a href="https://resumegenius.com/blog/resume-help/organizational-skills-examples-definition-guide">Organizational skills</a></li>
 		</ul>
 		<p>To help employers immediately recognize you meet their basic requirements, you may list key designer <a href="https://resumegenius.com/blog/resume-help/technical-skills">technical skills</a> in bullet points. Here’s an example of how to write your cover letter bullet points:</p>
 		<blockquote><p>The following are highlights of my skills and accomplishments:</p>
 			<ul>
 				<li><em>Designed 120+ webpages, using personalized HTML and CSS content</em></li>
 			</ul>
 		</blockquote>
 		<p>Here’s a list of key graphic designer technical skills:</p>
 		<ul>
 			<li aria-level="1">Color theory</li>
 			<li aria-level="1">Typography</li>
 			<li aria-level="1">Sketching</li>
 			<li aria-level="1"><a href="https://www.microsoft.com/en-ca/microsoft-365/visio/flowchart-software/">Microsoft Visio</a></li>
 			<li aria-level="1"><a href="https://www.coreldraw.com/en/">Corel Draw</a></li>
 			<li aria-level="1"><a href="https://www.sketchup.com/products/sketchup-pro">Trimble SketchUp Pro</a></li>
 			<li aria-level="1">Computer-aided design (CAD) software (<a href="https://www.autodesk.ca/en/products/autocad/overview">Autodesk AutoCAD</a>, <a href="https://www.autodesk.com/products/civil-3d/overview">Autodesk AutoCAD Civil 3D</a>, and <a href="https://www.bentley.com/en/products/brands/microstation">Bentley MicroStation</a>)</li>
 			<li aria-level="1">Presentation software (<a href="https://www.microsoft.com/en-ca/microsoft-365/powerpoint">Microsoft PowerPoint</a> and <a href="https://www.apple.com/ca/keynote/">Apple iWork Keynote</a>)</li>
 			<li aria-level="1">Adobe Systems (<a href="https://www.adobe.com/ca/products/photoshop.html">Adobe Photoshop</a>, <a href="https://www.adobe.com/ca/products/illustrator.html">Adobe Illustrator</a>, <a href="https://www.adobe.com/ca/products/indesign.html">Adobe InDesign</a>, and <a href="https://www.adobe.com/ca/creativecloud.html">Adobe Creative Cloud</a>)</li>
 			<li aria-level="1">Web development software (<a href="https://www.drupal.org/">Drupal</a>, HTML, <a href="https://www.javascript.com/">JavaScript</a>, and CSS)</li>
 		</ul>
 		<h3>2. Provide examples of your achievements (with numbers)</h3>
 		<p>Clients rely on your expertise to create visual compositions that are consistent with their brand. Whether you’re applying to a graphic design firm or advertising agency, you need to assure employers you can design or enhance visual elements to bring them closer to their business goals.</p>
 		<p>In your cover letter, provide examples of your most impressive achievements in a relevant role and use hard numbers to make your contributions sound more impactful. Doing so demonstrates to the hiring manager you can combine your artistic abilities with your technical skills to drive results for their organization.</p>
 		<aside class="tip">
 			<div class="tip-left">
 				<span>Tip</span>
 			</div>
 			<div class="tip-right">
 				<p>Just be sure you don’t copy-paste bullet points directly from your resume and put them in your cover letter, because it looks lazy to hiring managers.</p>
 			</div>
 		</aside>
 		<p>Here’s an example of how to effectively quantify your graphic designer skills in cover letter bullet points:</p>
 		<blockquote>
 			<ul>
 				<li><em>Lead a team of five designers to develop and implement the graphic, layout, and production communication materials while helping clients cut their costs by an average of 12%</em></li>
 				<li><em>Oversaw the efficient use of production project budgets ranging from $2,000-$25,000</em></li>
 				<li><em>Developed numerous marketing programs (logos, brochures, newsletters, infographics, presentations, and advertisements) that have improved client transactions by an average of 45%</em></li>
 			</ul>
 		</blockquote>
 		<h3>3. End with a convincing closing statement</h3>
 		<p>Your closing statement is your final opportunity to prove to the hiring manager you’re right for the job. So knowing <a href="https://resumegenius.com/blog/cover-letter-help/how-to-end-a-cover-letter">how to end your cover letter</a> in a convincing way will ensure your job application is successful.</p>
 		<p>To write a strong closing statement, show your passion for graphic design and reiterate your strengths in the field. Rather than talking about what you have to gain from employment, focus on how your skills and experience will benefit the company.</p>
 		<p>Here’s how to write a strong graphic designer closing statement:</p>
 		<ul>
 			<li aria-level="1">Restate your interest in the job by talking about what excites you the most about the opportunity.</li>
 			<li aria-level="1">State how your skills in graphic design will add value to the company or agency’s current operations. For example, if you’re applying to an advertising agency, explain how you can contribute to the development of brand strategies to increase ecommerce client revenue.</li>
 			<li aria-level="1">Thank the hiring manager for their time in reviewing your job application.</li>
 			<li aria-level="1">Include a call to action to encourage the hiring manager to get in touch. For example, “I look forward to elaborating on how my specific skills and abilities will benefit your agency. Please contact me by phone or via email to arrange a convenient time for us to meet.”</li>
 			<li aria-level="1">End with a professional sign-off, such as “Sincerely,” “Best Regards,” or “Thank you,”.</li>
 		</ul>
